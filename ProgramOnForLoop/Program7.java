package ProgramOnForLoop;

public class Program7 {
    public static void main(String[] args) {
        int h = 20;
        int i = 40;
        while (h <= 1) {
            System.out.println(h);
            h++;
        }
        while (i >= 20) {
            System.out.println(i);
            i++;
        }
    }
}